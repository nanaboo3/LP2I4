package com.example.trabalhofinal;

public class Aluno {

    public String nome, objetivo;
    public int idade, id;
    public double altura, peso;

    public Aluno(String nome, String objetivo, int idade, int id, double altura, double peso) {
        this.nome = nome;
        this.objetivo = objetivo;
        this.idade = idade;
        this.id = id;
        this.altura = altura;
        this.peso = peso;
    }

}
